def main_mskcc_finetune():
    """
    Fine-tune FairFace v3.2 on MSKCC dataset.
    Single-stage training with aggressive regularization for small data.
    """
    args = parse_args()
    os.makedirs(args.output_dir, exist_ok=True)

    if MSKCCDataset is None:
        print("ERROR: mskcc_dataset.py not found. Cannot use --finetune-mskcc.")
        sys.exit(1)

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*60}")
    print(f"MSKCC FINE-TUNING MODE")
    print(f"DEVICE: {device}")
    if device.type == "cuda":
        print(f"  GPU: {torch.cuda.get_device_name(0)}")
    print(f"{'='*60}\n")

    # ── Datasets ─────────────────────────────────────────────
    num_classes = 3  # Merged MST classes: Dark / Medium / Light
    print(f"--- Loading MSKCC datasets ({num_classes}-class mode) ---")
    train_ds = MSKCCDataset(
        args.mskcc_labels, args.crop_root,
        split="train", merge_classes=num_classes, image_size=args.image_size,
    )
    val_ds = MSKCCDataset(
        args.mskcc_labels, args.crop_root,
        split="val", merge_classes=num_classes, image_size=args.image_size,
    )
    test_ds = MSKCCDataset(
        args.mskcc_labels, args.crop_root,
        split="test", merge_classes=num_classes, image_size=args.image_size,
    )

    # Temperature-scaled sampling (temp=0.3 for 3-class)
    # Lower temp = more aggressive oversampling of Dark minority
    sample_weights = train_ds.get_sample_weights(strategy="temperature", temperature=0.3)

    # Epoch size: 2x the real dataset (~1360 samples)
    num_samples_per_epoch = len(train_ds) * 2

    sampler = WeightedRandomSampler(
        weights=sample_weights,
        num_samples=num_samples_per_epoch,
        replacement=True,
    )

    print(f"  WeightedRandomSampler ({num_samples_per_epoch:,} samples/epoch)")

    train_loader = DataLoader(
        train_ds, batch_size=args.batch_size, sampler=sampler,
        num_workers=args.num_workers, pin_memory=True, drop_last=True,
    )
    val_loader = DataLoader(
        val_ds, batch_size=args.batch_size, shuffle=False,
        num_workers=args.num_workers, pin_memory=True,
    )
    test_loader = DataLoader(
        test_ds, batch_size=args.batch_size, shuffle=False,
        num_workers=args.num_workers, pin_memory=True,
    )

    # ── Model (load checkpoint) ──────────────────────────────
    print("\n--- Building model from checkpoint ---")
    model = build_model(num_classes=5, pretrained=False)

    # Load v3.2 checkpoint first (5-class head), then replace head with 3-class
    if args.checkpoint and os.path.exists(args.checkpoint):
        state = torch.load(args.checkpoint, map_location=device, weights_only=True)
        model.load_state_dict(state)
        print(f"  ✓ Loaded backbone from checkpoint: {args.checkpoint}")
    else:
        print(f"  ⚠ No checkpoint provided — starting from ImageNet weights")
        model = build_model(num_classes=5, pretrained=True)

    # Replace classifier head: 5-class → num_classes (3)
    in_features = model.classifier[1].in_features
    model.classifier = nn.Sequential(
        nn.Dropout(p=args.mskcc_dropout),
        nn.Linear(in_features, num_classes),
    )
    print(f"  ✓ Classification head: {in_features} → {num_classes} classes")

    model = model.to(device)

    # Selective unfreeze
    unfreeze_from = 8 - args.mskcc_unfreeze_blocks  # blocks 0-7
    partial_unfreeze_backbone(model, unfreeze_from=unfreeze_from)
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"  Trainable: {trainable:,} params (last {args.mskcc_unfreeze_blocks} blocks + head)")
    print(f"  Dropout: {args.mskcc_dropout}")

    # ── Loss (Focal Loss + class weights + label smoothing) ─────
    # v6 showed the model defaults to Medium when uncertain (49-53% conf).
    # Focal Loss (gamma=2) down-weights easy Medium predictions and forces
    # the model to invest capacity in hard Dark↔Medium boundary cases.
    train_counts = np.bincount(train_ds._labels, minlength=num_classes).astype(float)
    # Inverse-sqrt weighting: less aggressive than full inverse-freq
    class_weights = 1.0 / np.sqrt(train_counts)
    class_weights = class_weights / class_weights.sum() * num_classes  # normalise to mean=1
    class_weights_tensor = torch.FloatTensor(class_weights).to(device)
    criterion = FocalLoss(gamma=2.0, weight=class_weights_tensor, label_smoothing=0.1)
    print(f"  Loss: FocalLoss (gamma=2.0, weighted, label_smoothing=0.1)")
    for c in range(num_classes):
        print(f"    {train_ds.class_names[c]}: weight={class_weights[c]:.3f} (n={int(train_counts[c])})")
    print(f"    → Easy predictions (conf>90%) get 0.01× gradient")
    print(f"    → Hard predictions (conf~50%) get 0.25× gradient")

    # ── Optimizer ────────────────────────────────────────────
    optimizer = optim.AdamW(
        filter(lambda p: p.requires_grad, model.parameters()),
        lr=args.mskcc_lr,
        weight_decay=args.mskcc_weight_decay,
    )
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer, T_0=10, T_mult=2, eta_min=1e-7,
    )
    scaler = GradScaler(enabled=(device.type == "cuda"))

    print(f"  LR: {args.mskcc_lr}, Weight Decay: {args.mskcc_weight_decay}")
    print(f"  Scheduler: CosineAnnealingWarmRestarts(T_0=10, T_mult=2)")
    print(f"  MixUp: Disabled (small dataset)")

    # ── Training loop ────────────────────────────────────────
    history = {
        "train_loss": [], "train_acc": [],
        "val_loss": [], "val_acc": [],
    }
    best_val_acc = 0.0
    epochs_no_improve = 0

    print(f"\n{'='*60}")
    print(f"TRAINING ({args.mskcc_epochs} epochs, no MixUp)")
    print(f"{'='*60}")

    for epoch in range(args.mskcc_epochs):
        t0 = time.time()

        train_loss, train_acc = train_one_epoch(
            model, train_loader, criterion, optimizer, scaler, device,
            mixup_alpha=0.0,  # No MixUp for small dataset
        )
        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, device)
        scheduler.step()
        dt = time.time() - t0

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        improved = ""
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            torch.save(model.state_dict(),
                       os.path.join(args.output_dir, "fairface_mskcc_best.pth"))
            improved = " ★ saved"
            epochs_no_improve = 0
        else:
            epochs_no_improve += 1

        print(
            f"  Epoch {epoch+1:02d}/{args.mskcc_epochs}  "
            f"loss={train_loss:.4f}  acc={train_acc:.4f}  "
            f"val_loss={val_loss:.4f}  val_acc={val_acc:.4f}  "
            f"lr={optimizer.param_groups[0]['lr']:.2e}  "
            f"({dt:.1f}s){improved}"
        )

        if epochs_no_improve >= args.patience:
            print(f"\n  ⚠ Early stopping at epoch {epoch+1}")
            break

    # ── Final model save ─────────────────────────────────────
    torch.save(model.state_dict(),
               os.path.join(args.output_dir, "fairface_mskcc_final.pth"))

    # ── Evaluation on TEST split ──────────────────────────────
    # Test split is better balanced (~20 images/class) than val.
    # Val is still used for early stopping / model selection above.
    print(f"\n{'='*60}")
    print("FINAL EVALUATION — TEST SPLIT (best MSKCC fine-tuned model)")
    print(f"{'='*60}")

    best_path = os.path.join(args.output_dir, "fairface_mskcc_best.pth")
    if os.path.exists(best_path):
        model.load_state_dict(
            torch.load(best_path, map_location=device, weights_only=True)
        )

    _, test_acc, test_preds, test_labels = evaluate(
        model, test_loader, criterion, device
    )

    class_names = test_ds.class_names
    print(f"  Evaluating with {len(class_names)} classes: {class_names}")
    print_evaluation_report(test_labels, test_preds, class_names)

    plot_confusion_matrix(
        test_labels, test_preds, class_names,
        os.path.join(args.output_dir, "mskcc_confusion_matrix.png"),
    )
    plot_training_history(
        history,
        os.path.join(args.output_dir, "mskcc_training_curves.png"),
    )

    # TTA evaluation on test split
    print(f"\n{'='*60}")
    print("TTA EVALUATION — TEST SPLIT (5 augmented views + 1 clean)")
    print(f"{'='*60}")
    tta_preds, tta_labels = evaluate_tta(
        model, test_ds, device,
        num_augments=5, batch_size=args.batch_size,
        image_size=args.image_size, num_workers=args.num_workers,
    )
    print_evaluation_report(tta_labels, tta_preds, class_names)
    plot_confusion_matrix(
        tta_labels, tta_preds, class_names,
        os.path.join(args.output_dir, "mskcc_confusion_matrix_tta.png"),
    )

    print(f"\n{'='*60}")
    print(f"✓ MSKCC fine-tuning complete!")
    print(f"  Best val accuracy (early stop): {best_val_acc:.4f}")
    print(f"  Test accuracy: {test_acc:.4f}")
    print(f"  Best model: {best_path}")
    print(f"  Output: {args.output_dir}")
    print(f"{'='*60}")


if __name__ == "__main__":
    args = parse_args()
    if args.finetune_mskcc:
        main_mskcc_finetune()
    else:
        main()
