class OrdinalCrossEntropy(nn.Module):
    """
    Cross-entropy with soft ordinal labels and per-class weighting.

    Instead of hard one-hot targets, generates a Gaussian distribution
    centred on the true class. Adjacent classes receive partial credit,
    so the model is penalised much less for near-miss predictions
    (e.g. Type III → Type IV) than for far-off errors (Type III → Type I).

    Args:
        num_classes:   Number of ordinal classes
        sigma:         Gaussian spread. Lower = stricter (less credit for near-misses).
                       At sigma=0.7, adjacent classes get ~7% of peak weight.
        gamma:         Focal focusing parameter. Higher = more focus on hard samples.
        class_weights: Optional (C,) tensor of per-class loss multipliers.
                       Use inverse-frequency weights to penalise minority-class
                       errors more heavily (breaks dominant-class attractors).
    """

    def __init__(self, num_classes: int = 10, sigma: float = 1.0, gamma: float = 2.0,
                 class_weights: torch.Tensor = None):
        super().__init__()
        self.num_classes = num_classes
        self.sigma = sigma
        self.gamma = gamma

        # Pre-compute the soft label matrix (C x C)
        # soft_labels[k] = normalised Gaussian centred on class k
        indices = torch.arange(num_classes, dtype=torch.float32)
        soft = torch.zeros(num_classes, num_classes)
        for k in range(num_classes):
            dist = (indices - k) ** 2
            weights = torch.exp(-dist / (2 * sigma ** 2))
            soft[k] = weights / weights.sum()  # normalise to valid distribution
        self.register_buffer("soft_labels", soft)

        # Per-class loss weights (default: uniform)
        if class_weights is not None:
            self.register_buffer("class_weights", class_weights.float())
        else:
            self.register_buffer("class_weights", torch.ones(num_classes))

    def forward(self, logits, targets):
        """
        Args:
            logits:  (B, C) raw model outputs
            targets: (B,) integer class labels
        """
        # Look up soft target distribution for each sample
        soft_targets = self.soft_labels[targets]  # (B, C)

        # Cast to float32 and clamp to avoid NaN from float16 overflow
        # under autocast. The model may produce inf logits in float16
        # which stay inf even after float32 promotion.
        logits = logits.float().clamp(-100, 100)
        log_probs = nn.functional.log_softmax(logits, dim=1)

        # KL-divergence style loss: -sum(soft_target * log_prob)
        per_sample_loss = -(soft_targets * log_probs).sum(dim=1)  # (B,)

        # Per-class weighting: amplify loss for minority classes
        sample_weights = self.class_weights[targets]  # (B,)
        per_sample_loss = per_sample_loss * sample_weights

        # Optional focal weighting
        if self.gamma > 0:
            probs = torch.exp(log_probs)
            # p_t = probability assigned to the TRUE class
            pt = probs.gather(1, targets.unsqueeze(1)).squeeze(1)
            focal_weight = (1 - pt) ** self.gamma
            per_sample_loss = focal_weight * per_sample_loss

        return per_sample_loss.mean()


class FocalLoss(nn.Module):
    """
    Focal Loss (Lin et al., 2017) with class weights and label smoothing.

    Standard CE treats all predictions equally. Focal loss multiplies
    each sample's loss by (1 - p_t)^gamma, where p_t is the model's
    confidence in the correct class:

      - Easy samples (p_t ≈ 0.95): weight = 0.0025 → nearly ignored
      - Hard samples (p_t ≈ 0.50): weight = 0.2500 → fully trained on
      - Very hard (p_t ≈ 0.30):    weight = 0.4900 → extra focus

    This forces the model to stop coasting on easy majority-class
    predictions and invest capacity in hard boundary cases.

    Args:
        gamma:           Focusing parameter. 0 = standard CE, 2 = recommended.
        weight:          Optional (C,) tensor of per-class weights.
        label_smoothing: Smoothing factor (0 = hard labels, 0.1 = recommended).
    """

    def __init__(self, gamma: float = 2.0, weight: torch.Tensor = None,
                 label_smoothing: float = 0.0):
        super().__init__()
        self.gamma = gamma
        self.label_smoothing = label_smoothing
        if weight is not None:
            self.register_buffer("weight", weight.float())
        else:
            self.weight = None

    def forward(self, logits, targets):
        """
        Args:
            logits:  (B, C) raw model outputs
            targets: (B,) integer class labels
        """
        # Standard cross-entropy with label smoothing
        ce_loss = nn.functional.cross_entropy(
            logits, targets, weight=self.weight,
            label_smoothing=self.label_smoothing, reduction="none",
        )  # (B,)

        # Focal modulation: (1 - p_t)^gamma
        log_probs = nn.functional.log_softmax(logits.float(), dim=1)
        probs = torch.exp(log_probs)
        pt = probs.gather(1, targets.unsqueeze(1)).squeeze(1)  # p_t for true class
        focal_weight = (1.0 - pt) ** self.gamma

        loss = focal_weight * ce_loss
        return loss.mean()


