def main():
    args = parse_args()
    os.makedirs(args.output_dir, exist_ok=True)

    # ------------------------------------------------------------------
    # Device setup
    # ------------------------------------------------------------------
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*60}")
    print(f"DEVICE: {device}")
    if device.type == "cuda":
        print(f"  GPU: {torch.cuda.get_device_name(0)}")
        print(f"  Memory: {torch.cuda.get_device_properties(0).total_memory / 1e9:.1f} GB")

    print(f"{'='*60}\n")

    # ------------------------------------------------------------------
    # Datasets & DataLoaders
    # ------------------------------------------------------------------
    print("--- Loading datasets ---")
    train_ds = FairFaceDataset(
        args.data_csv, args.image_root,
        split="train", image_size=args.image_size,
        num_classes=args.num_classes,
        max_samples_per_class=args.max_samples_per_class,
    )
    val_ds = FairFaceDataset(
        args.data_csv, args.image_root,
        split="val", image_size=args.image_size,
        num_classes=args.num_classes,
    )

    # --- WeightedRandomSampler for balanced training ---
    sample_weights = train_ds.get_sample_weights()

    # Compute balanced epoch size: target ≈ largest_class × num_classes
    # so every class is effectively drawn ~largest_class times per epoch.
    class_counts = np.bincount(train_ds._labels, minlength=args.num_classes)
    target_per_class = int(class_counts.max())
    num_samples_balanced = target_per_class * args.num_classes

    sampler = WeightedRandomSampler(
        weights=sample_weights,
        num_samples=num_samples_balanced,
        replacement=True,
    )

    # Log effective oversampling factors
    print(f"  ✓ WeightedRandomSampler active ({num_samples_balanced:,} samples/epoch)")
    print(f"    Target per class: ~{target_per_class:,}")
    for cls_id in range(args.num_classes):
        cnt = int(class_counts[cls_id])
        factor = target_per_class / cnt if cnt > 0 else 0
        print(f"    {train_ds.class_names[cls_id]}: {cnt:,} images → ~{target_per_class:,}/epoch ({factor:.1f}× oversample)")

    train_loader = DataLoader(
        train_ds, batch_size=args.batch_size, sampler=sampler,
        num_workers=args.num_workers, pin_memory=True, drop_last=True,
    )
    val_loader = DataLoader(
        val_ds, batch_size=args.batch_size, shuffle=False,
        num_workers=args.num_workers, pin_memory=True,
    )

    # ------------------------------------------------------------------
    # Model
    # ------------------------------------------------------------------
    print("\n--- Building EfficientNet-B4 model ---")
    model = build_model(num_classes=args.num_classes)
    model = model.to(device)
    total_params = sum(p.numel() for p in model.parameters())
    print(f"  Total parameters: {total_params:,}")

    # ------------------------------------------------------------------
    # Loss function — Ordinal Cross-Entropy
    # ------------------------------------------------------------------
    # v3.2: Reverted to v3.0 baseline (sigma=1.0, no class weights).
    # v3.1's class weights + tight sigma (0.8) caused a 77%→71% regression
    # because they distorted the training signal on noisy labels.
    criterion = OrdinalCrossEntropy(
        num_classes=args.num_classes, sigma=1.0, gamma=2.0,
    ).to(device)
    print(f"\n  Loss: OrdinalCrossEntropy(sigma=1.0, gamma=2.0)")
    print(f"  Soft labels give adjacent classes ~15% credit (v3.0 baseline)")

    # ------------------------------------------------------------------
    # Mixed precision scaler
    # ------------------------------------------------------------------
    scaler = GradScaler(enabled=(device.type == "cuda"))

    # ------------------------------------------------------------------
    # Training history
    # ------------------------------------------------------------------
    history = {
        "train_loss": [], "train_acc": [],
        "val_loss": [], "val_acc": [],
    }

    best_val_loss = float("inf")
    best_val_acc = 0.0
    epochs_no_improve = 0

    # ==================================================================
    # STAGE 1: Train head only
    # ==================================================================
    print(f"\n{'='*60}")
    print(f"STAGE 1: Head Training ({args.epochs_head} epochs)")
    print(f"{'='*60}")

    freeze_backbone(model)
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"  Trainable parameters: {trainable:,}")

    optimizer = optim.AdamW(
        filter(lambda p: p.requires_grad, model.parameters()),
        lr=1e-3,
    )

    for epoch in range(args.epochs_head):
        t0 = time.time()
        train_loss, train_acc = train_one_epoch(
            model, train_loader, criterion, optimizer, scaler, device
        )
        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, device)
        dt = time.time() - t0

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        improved = ""
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            torch.save(model.state_dict(),
                       os.path.join(args.output_dir, "best_head_model.pth"))
            improved = " ★ saved"

        print(
            f"  Epoch {epoch+1:02d}/{args.epochs_head}  "
            f"loss={train_loss:.4f}  acc={train_acc:.4f}  "
            f"val_loss={val_loss:.4f}  val_acc={val_acc:.4f}  "
            f"({dt:.1f}s){improved}"
        )

    # Load best head model before fine-tuning
    model.load_state_dict(
        torch.load(os.path.join(args.output_dir, "best_head_model.pth"),
                    map_location=device, weights_only=True)
    )

    # ==================================================================
    # STAGE 2: Fine-tune entire network
    # ==================================================================
    print(f"\n{'='*60}")
    print(f"STAGE 2: Full Fine-Tuning ({args.epochs_finetune} epochs)")
    print(f"{'='*60}")

    partial_unfreeze_backbone(model, unfreeze_from=4)
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"  Trainable parameters: {trainable:,}  (last 4 blocks + head)")

    history["stage_boundary"] = args.epochs_head

    optimizer = optim.AdamW(model.parameters(), lr=1.2e-5, weight_decay=1e-5)
    # v3.1: CosineAnnealingWarmRestarts for smoother LR decay.
    # T_0=15 = first restart after 15 epochs, T_mult=2 = subsequent
    # cycles double (30, 60...). Reduces the val_acc oscillation
    # seen with ReduceLROnPlateau in v3.0.
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer,
        T_0=15,
        T_mult=2,
        eta_min=1e-7,
    )

    for epoch in range(args.epochs_finetune):
        global_epoch = args.epochs_head + epoch
        t0 = time.time()

        train_loss, train_acc = train_one_epoch(
            model, train_loader, criterion, optimizer, scaler, device,
            mixup_alpha=0.3,  # re-enabled — ITA bins are well-separated
        )
        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, device)
        scheduler.step(epoch)  # CosineAnnealing uses epoch number
        dt = time.time() - t0

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        # Track best by val_acc (ordinal loss can plateau while acc still improves)
        improved = ""
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            best_val_loss = val_loss
            torch.save(model.state_dict(),
                       os.path.join(args.output_dir, "best_finetuned_model.pth"))
            improved = " ★ saved"
            epochs_no_improve = 0
        else:
            epochs_no_improve += 1

        print(
            f"  Epoch {global_epoch+1:02d}/{args.epochs_head + args.epochs_finetune}  "
            f"loss={train_loss:.4f}  acc={train_acc:.4f}  "
            f"val_loss={val_loss:.4f}  val_acc={val_acc:.4f}  "
            f"lr={optimizer.param_groups[0]['lr']:.2e}  "
            f"({dt:.1f}s){improved}"
        )

        # Early stopping
        if epochs_no_improve >= args.patience:
            print(f"\n  ⚠ Early stopping at epoch {global_epoch+1} "
                  f"(no improvement for {args.patience} epochs)")
            break

    # ------------------------------------------------------------------
    # Save final model
    # ------------------------------------------------------------------
    torch.save(model.state_dict(),
               os.path.join(args.output_dir, "final_model.pth"))
    print(f"\n✓ Final model saved to {os.path.join(args.output_dir, 'final_model.pth')}")

    # ==================================================================
    # EVALUATION
    # ==================================================================
    print(f"\n{'='*60}")
    print("FINAL EVALUATION (best fine-tuned model)")
    print(f"{'='*60}")

    # Load best model
    best_path = os.path.join(args.output_dir, "best_finetuned_model.pth")
    if os.path.exists(best_path):
        model.load_state_dict(
            torch.load(best_path, map_location=device, weights_only=True)
        )

    _, val_acc, val_preds, val_labels = evaluate(
        model, val_loader, criterion, device
    )

    class_names = val_ds.class_names

    # Classification report
    print_evaluation_report(val_labels, val_preds, class_names)

    # Confusion matrix
    plot_confusion_matrix(
        val_labels, val_preds, class_names,
        os.path.join(args.output_dir, "fairface_confusion_matrix.png"),
    )

    # Training curves
    plot_training_history(
        history,
        os.path.join(args.output_dir, "fairface_training_curves.png"),
    )

    # Latent space visualisation
    print("\n--- Extracting validation embeddings ---")
    embeddings, embed_labels = extract_embeddings(model, val_loader, device)
    print(f"  Embeddings shape: {embeddings.shape}")
    plot_latent_space(
        embeddings, embed_labels, class_names,
        os.path.join(args.output_dir, "fairface_latent_space.png"),
    )

    # ------------------------------------------------------------------
    # TTA Evaluation (v3.1) — compare against standard single-view
    # ------------------------------------------------------------------
    print(f"\n{'='*60}")
    print("TTA EVALUATION (5 augmented views + 1 clean)")
    print(f"{'='*60}")

    tta_preds, tta_labels = evaluate_tta(
        model, val_ds, device,
        num_augments=5, batch_size=args.batch_size,
        image_size=args.image_size, num_workers=args.num_workers,
    )
    print_evaluation_report(tta_labels, tta_preds, class_names)
    plot_confusion_matrix(
        tta_labels, tta_preds, class_names,
        os.path.join(args.output_dir, "fairface_confusion_matrix_tta.png"),
    )

    print(f"\n{'='*60}")
    print(f"✓ Training complete!")
    print(f"  Best validation accuracy: {best_val_acc:.4f}")
    print(f"  Models saved to: {args.output_dir}")
    print(f"{'='*60}")


