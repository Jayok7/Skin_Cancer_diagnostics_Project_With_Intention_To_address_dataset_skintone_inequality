#!/usr/bin/env python3
"""
FairFace PyTorch Dataset
========================
A PyTorch Dataset class for loading FairFace images paired with
L*-based skin tone labels (0-5) generated by compute_fairface_labels.py.

Provides separate train/val transform pipelines that mirror the
augmentation strategy from the existing train_fitzpatrick.py.

Class-aware augmentation: minority classes (below a frequency threshold)
receive stronger augmentations to improve visual diversity when
oversampled by WeightedRandomSampler.
"""

import os
import warnings
import numpy as np
import pandas as pd
from PIL import Image

import torch
from torch.utils.data import Dataset, WeightedRandomSampler
from torchvision import transforms


# ========================================================================
# TRANSFORM FACTORIES
# ========================================================================

def get_train_transforms(image_size: int = 380):
    """
    Standard training augmentations for majority classes.
    """
    return transforms.Compose([
        transforms.Resize(image_size + 32),
        transforms.RandomCrop(image_size),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.ColorJitter(
            brightness=0.3,
            contrast=0.3,
            saturation=0.3,
            hue=0.08,
        ),
        transforms.RandomGrayscale(p=0.02),
        transforms.GaussianBlur(kernel_size=5, sigma=(0.1, 2.0)),
        transforms.ToTensor(),
        transforms.Normalize(
            mean=[0.485, 0.456, 0.406],
            std=[0.229, 0.224, 0.225],
        ),
        transforms.RandomErasing(p=0.15, scale=(0.02, 0.15)),
    ])


def get_minority_train_transforms(image_size: int = 380):
    """
    Mildly stronger augmentations for minority classes (e.g. Type II, III).

    Only slightly more aggressive ColorJitter and RandomErasing compared
    to the standard transforms. Avoids geometric distortions (rotation,
    perspective) which warp face shapes unrealistically and hurt
    generalisation when oversampled by WeightedRandomSampler.
    """
    return transforms.Compose([
        transforms.Resize(image_size + 40),          # slightly larger crop pool
        transforms.RandomCrop(image_size),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.ColorJitter(
            brightness=0.35,
            contrast=0.35,
            saturation=0.35,
            hue=0.09,
        ),
        transforms.RandomGrayscale(p=0.03),
        transforms.GaussianBlur(kernel_size=5, sigma=(0.1, 2.0)),
        transforms.ToTensor(),
        transforms.Normalize(
            mean=[0.485, 0.456, 0.406],
            std=[0.229, 0.224, 0.225],
        ),
        transforms.RandomErasing(p=0.20, scale=(0.02, 0.15)),
    ])


def get_val_transforms(image_size: int = 380):
    """
    Validation / inference transforms — deterministic resize + crop only.
    """
    return transforms.Compose([
        transforms.Resize(image_size + 32),
        transforms.CenterCrop(image_size),
        transforms.ToTensor(),
        transforms.Normalize(
            mean=[0.485, 0.456, 0.406],
            std=[0.229, 0.224, 0.225],
        ),
    ])


# ========================================================================
# DATASET CLASS
# ========================================================================

class FairFaceDataset(Dataset):
    """
    PyTorch Dataset for FairFace images with L*-based skin tone labels.

    Args:
        csv_path:    Path to fairface_lstar_labels.csv
        image_root:  Root directory containing train/ and val/ image subdirs
        split:       'train', 'val', or 'all' — filters by original_split column
        transform:   torchvision transform pipeline (if None, uses defaults)
        image_size:  Image size for default transforms (default: 380 for EfficientNet-B4)
        num_classes: Number of output classes (6 or 3)
        minority_threshold: Classes with fewer than this fraction of samples
                            get stronger augmentations (default: 0.10 = 10%)

    Returns:
        (image_tensor, label) where label is an integer in [0, 5] (or [0, 2] for 3-way)
    """

    # Human-readable class names for plotting / evaluation
    FITZPATRICK_6_NAMES = [
        "Type VI (darkest)",   # class 0
        "Type V",              # class 1
        "Type IV",             # class 2
        "Type III",            # class 3
        "Type II",             # class 4
        "Type I (lightest)",   # class 5
    ]

    MST10_NAMES = [
        "MST 10 (deepest)",    # class 0
        "MST 9",               # class 1
        "MST 8",               # class 2
        "MST 7",               # class 3
        "MST 6",               # class 4
        "MST 5",               # class 5
        "MST 4",               # class 6
        "MST 3",               # class 7
        "MST 2",               # class 8
        "MST 1 (lightest)",    # class 9
    ]

    MST5_NAMES = [
        "Very Dark (MST 9-10)",    # class 0
        "Dark (MST 7-8)",          # class 1
        "Medium (MST 5-6)",        # class 2
        "Light (MST 3-4)",         # class 3
        "Very Light (MST 1-2)",    # class 4
    ]

    def __init__(
        self,
        csv_path: str,
        image_root: str,
        split: str = "all",
        transform=None,
        image_size: int = 380,
        num_classes: int = 10,
        minority_threshold: float = 0.10,
        max_samples_per_class: int = 0,
    ):
        self.image_root = image_root
        self.df = pd.read_csv(csv_path)
        self.num_classes = num_classes
        self.split = split

        # Filter by split
        if split != "all":
            self.df = self.df[self.df["original_split"] == split].reset_index(drop=True)

        # Validate paths — warn once but don't crash
        sample_path = os.path.join(image_root, self.df.iloc[0]["file"]) if len(self.df) > 0 else ""
        if sample_path and not os.path.isfile(sample_path):
            warnings.warn(
                f"Sample image not found: {sample_path}. "
                f"Check that --image-root points to the FairFace image directory."
            )

        # ---------------------------------------------------------------
        # Determine label column and class names based on num_classes
        # and what columns are available in the CSV.
        # ---------------------------------------------------------------
        has_mst = "mst10_class" in self.df.columns
        has_fitz = "skin_tone_class" in self.df.columns

        if num_classes == 10:
            # MST-10 mode
            assert has_mst, "CSV must have 'mst10_class' column for 10-class mode. Run compute_mst_labels.py first."
            self._labels = self.df["mst10_class"].values.copy()
            self.class_names = self.MST10_NAMES

        elif num_classes == 5:
            # MST-5 mode (grouped pairs)
            assert has_mst, "CSV must have 'mst5_class' column for 5-class mode. Run compute_mst_labels.py first."
            self._labels = self.df["mst5_class"].values.copy()
            self.class_names = self.MST5_NAMES

        elif num_classes == 6:
            # Legacy Fitzpatrick 6-way
            assert has_fitz, "CSV must have 'skin_tone_class' column for 6-class mode."
            self._labels = self.df["skin_tone_class"].values.copy()
            self.class_names = self.FITZPATRICK_6_NAMES

        elif num_classes == 3:
            # Legacy Fitzpatrick 3-way
            assert has_fitz, "CSV must have 'skin_tone_class' column for 3-class mode."
            self._labels = self.df["skin_tone_class"].values.copy() // 2
            self.class_names = ["Dark", "Medium", "Light"]

        else:
            raise ValueError(f"num_classes must be 3, 5, 6, or 10 — got {num_classes}")

        # ---------------------------------------------------------------
        # Undersample majority classes (if cap is set)
        # ---------------------------------------------------------------
        if max_samples_per_class > 0 and split == "train":
            keep_mask = np.ones(len(self._labels), dtype=bool)
            rng = np.random.default_rng(42)  # reproducible
            for cls_id in range(self.num_classes):
                cls_indices = np.where(self._labels == cls_id)[0]
                if len(cls_indices) > max_samples_per_class:
                    drop = rng.choice(
                        cls_indices,
                        size=len(cls_indices) - max_samples_per_class,
                        replace=False,
                    )
                    keep_mask[drop] = False
            n_before = len(self.df)
            self.df = self.df[keep_mask].reset_index(drop=True)
            self._labels = self._labels[keep_mask]
            print(f"  ⚖ Undersampled: {n_before:,} → {len(self.df):,} " +
                  f"(cap={max_samples_per_class:,}/class)")

        # Identify minority classes (below threshold fraction)
        counts = np.bincount(self._labels, minlength=self.num_classes)
        total = len(self._labels)
        fractions = counts / total
        self._minority_classes = set(
            int(c) for c in range(self.num_classes) if fractions[c] < minority_threshold
        )

        # Assign transforms — class-aware for training split
        if transform is not None:
            self.transform = transform
            self._minority_transform = transform
        elif split == "train":
            self.transform = get_train_transforms(image_size)
            self._minority_transform = get_minority_train_transforms(image_size)
        else:
            self.transform = get_val_transforms(image_size)
            self._minority_transform = self.transform

        # Log info
        minority_str = ", ".join(
            f"{self.class_names[c]} ({fractions[c]:.1%})"
            for c in sorted(self._minority_classes)
        ) or "none"
        print(
            f"✓ FairFaceDataset: {len(self.df):,} images  |  split={split}  "
            f"|  size={image_size}  |  classes={self.num_classes}"
        )
        if split == "train":
            print(f"  Minority classes (< {minority_threshold:.0%}): {minority_str}")

    def __len__(self) -> int:
        return len(self.df)

    def __getitem__(self, idx: int):
        row = self.df.iloc[idx]
        img_path = os.path.join(self.image_root, row["file"])

        # Load image — handle missing files gracefully
        try:
            image = Image.open(img_path).convert("RGB")
        except (FileNotFoundError, OSError):
            warnings.warn(f"Could not load image: {img_path}")
            image = Image.new("RGB", (380, 380), (128, 128, 128))

        label = self._labels[idx]

        # Apply class-aware transform: minority classes get stronger augmentation
        if self.split == "train" and label in self._minority_classes:
            image = self._minority_transform(image)
        else:
            if self.transform:
                image = self.transform(image)

        return image, int(label)

    def get_class_weights(self) -> torch.Tensor:
        """
        Compute inverse-frequency class weights for balanced training.
        Returns a tensor of shape (num_classes,) suitable for
        passing to a weighted loss function.
        """
        from sklearn.utils.class_weight import compute_class_weight

        unique = np.unique(self._labels)
        weights = compute_class_weight("balanced", classes=unique, y=self._labels)

        # Build a full weight vector even if some classes are missing
        full_weights = np.ones(self.num_classes, dtype=np.float32)
        for cls, w in zip(unique, weights):
            full_weights[int(cls)] = w

        return torch.tensor(full_weights, dtype=torch.float32)

    def get_sample_weights(self) -> torch.Tensor:
        """
        Compute per-sample weights for WeightedRandomSampler.

        Each sample gets weight = 1 / (class_frequency), so minority
        classes are drawn more often, effectively balancing the
        distribution within each epoch.

        Returns:
            Tensor of shape (N,) — one weight per sample.
        """
        counts = np.bincount(self._labels, minlength=self.num_classes).astype(np.float64)
        # Avoid division by zero for missing classes
        counts[counts == 0] = 1.0
        class_weights = 1.0 / counts
        sample_weights = class_weights[self._labels]
        return torch.tensor(sample_weights, dtype=torch.float64)
