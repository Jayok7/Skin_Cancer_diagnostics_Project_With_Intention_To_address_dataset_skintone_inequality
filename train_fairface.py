#!/usr/bin/env python3
"""
FairFace Skin Tone Classifier — PyTorch Training Script
========================================================
Trains a 6-way Fitzpatrick skin-tone classifier on FairFace images
using pseudo-labels generated by compute_fairface_labels.py.

Architecture mirrors the 2-stage strategy from train_fitzpatrick.py:
  Stage 1: Train classification head only (backbone frozen)
  Stage 2: Fine-tune entire network at low learning rate

Usage:
    python train_fairface.py \
        --data-csv datasets/fairface_lstar_labels.csv \
        --image-root datasets/fairface-img-margin025-trainval/ \
        --output-dir outputs/fairface/ \
        --batch-size 32 \
        --epochs-head 10 \
        --epochs-finetune 40
"""

import os
import sys
import argparse
import time

import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")  # Non-interactive backend for cluster / headless
import matplotlib.pyplot as plt
import seaborn as sns

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader
from torch.cuda.amp import GradScaler, autocast
from torchvision import models

from sklearn.metrics import (
    classification_report,
    confusion_matrix,
    accuracy_score,
    precision_recall_fscore_support,
)

from fairface_dataset import FairFaceDataset

# ========================================================================
# CONFIGURATION
# ========================================================================

def parse_args():
    p = argparse.ArgumentParser(description="Train FairFace Skin Tone Classifier")
    p.add_argument("--data-csv", type=str, required=True,
                   help="Path to fairface_lstar_labels.csv")
    p.add_argument("--image-root", type=str, required=True,
                   help="Root dir of FairFace images")
    p.add_argument("--output-dir", type=str, default="./outputs/fairface",
                   help="Output directory for models & plots")
    p.add_argument("--batch-size", type=int, default=32,
                   help="Batch size (default: 32)")
    p.add_argument("--epochs-head", type=int, default=10,
                   help="Epochs for head-only training (stage 1)")
    p.add_argument("--epochs-finetune", type=int, default=40,
                   help="Epochs for full fine-tuning (stage 2)")
    p.add_argument("--image-size", type=int, default=380,
                   help="Image size in pixels (default: 380 for EfficientNet-B4)")
    p.add_argument("--num-workers", type=int, default=4,
                   help="DataLoader workers (default: 4)")
    p.add_argument("--patience", type=int, default=15,
                   help="Early stopping patience (default: 15)")
    p.add_argument("--num-classes", type=int, default=6, choices=[3, 6],
                   help="Number of skin tone classes (6 or 3)")
    return p.parse_args()


# ========================================================================
# FOCAL LOSS  (PyTorch port of train_fitzpatrick.py focal_loss)
# ========================================================================

class FocalLoss(nn.Module):
    """
    Multi-class focal loss as described in Lin et al. (2017).
    Reduces the relative loss for well-classified examples and
    focuses training on hard, misclassified examples.

    Args:
        gamma: Focusing parameter (default: 2.0)
        alpha: Balancing factor (default: 0.25)
        weight: Per-class weights tensor (optional)
    """

    def __init__(self, gamma: float = 2.0, alpha: float = 0.25, weight=None):
        super().__init__()
        self.gamma = gamma
        self.alpha = alpha
        self.weight = weight

    def forward(self, logits, targets):
        """
        Args:
            logits: (B, C) raw model outputs (before softmax)
            targets: (B,) integer class labels
        """
        ce_loss = nn.functional.cross_entropy(
            logits, targets, weight=self.weight, reduction="none"
        )
        pt = torch.exp(-ce_loss)  # p_t = probability of correct class
        focal_weight = self.alpha * (1 - pt) ** self.gamma
        return (focal_weight * ce_loss).mean()


# ========================================================================
# MODEL
# ========================================================================

def build_model(num_classes: int = 6, pretrained: bool = True):
    """
    Build EfficientNet-B4 with a custom classification head.

    Why B4: Strong accuracy/compute balance. 380×380 native input,
    1792 feature dim, 19M params — excellent for skin tone classification
    on GPU clusters.

    Returns:
        model, backbone (for selective freezing)
    """
    weights = models.EfficientNet_B4_Weights.IMAGENET1K_V1 if pretrained else None
    model = models.efficientnet_b4(weights=weights)

    # Replace the classifier head
    # EfficientNet-B4 features dim = 1792
    in_features = model.classifier[1].in_features
    model.classifier = nn.Sequential(
        nn.Dropout(p=0.5),
        nn.Linear(in_features, num_classes),
    )

    return model


def freeze_backbone(model):
    """Freeze all layers except the classifier head."""
    for name, param in model.named_parameters():
        if "classifier" not in name:
            param.requires_grad = False


def unfreeze_backbone(model):
    """Unfreeze all layers for fine-tuning."""
    for param in model.parameters():
        param.requires_grad = True


# ========================================================================
# TRAINING LOOP
# ========================================================================

def train_one_epoch(model, loader, criterion, optimizer, scaler, device):
    """Run one training epoch with mixed-precision support."""
    model.train()
    running_loss = 0.0
    correct = 0
    total = 0

    for images, labels in loader:
        images = images.to(device)
        labels = labels.to(device)

        optimizer.zero_grad()

        with autocast(device_type="cuda", enabled=(device.type == "cuda")):
            outputs = model(images)
            loss = criterion(outputs, labels)

        scaler.scale(loss).backward()
        scaler.step(optimizer)
        scaler.update()

        running_loss += loss.item() * images.size(0)
        _, preds = outputs.max(1)
        correct += preds.eq(labels).sum().item()
        total += labels.size(0)

    epoch_loss = running_loss / total
    epoch_acc = correct / total
    return epoch_loss, epoch_acc


@torch.no_grad()
def evaluate(model, loader, criterion, device):
    """Evaluate on a validation / test set."""
    model.eval()
    running_loss = 0.0
    correct = 0
    total = 0
    all_preds = []
    all_labels = []

    for images, labels in loader:
        images = images.to(device)
        labels = labels.to(device)

        with autocast(device_type="cuda", enabled=(device.type == "cuda")):
            outputs = model(images)
            loss = criterion(outputs, labels)

        running_loss += loss.item() * images.size(0)
        _, preds = outputs.max(1)
        correct += preds.eq(labels).sum().item()
        total += labels.size(0)

        all_preds.extend(preds.cpu().numpy())
        all_labels.extend(labels.cpu().numpy())

    epoch_loss = running_loss / total
    epoch_acc = correct / total
    return epoch_loss, epoch_acc, np.array(all_preds), np.array(all_labels)


# ========================================================================
# PLOTTING & EVALUATION
# ========================================================================

def plot_training_history(history: dict, output_path: str):
    """Save training curves (loss + accuracy) as a figure."""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))

    ax1.plot(history["train_loss"], label="Train Loss", linewidth=2)
    ax1.plot(history["val_loss"], label="Val Loss", linewidth=2)
    ax1.set_xlabel("Epoch")
    ax1.set_ylabel("Loss")
    ax1.set_title("Training & Validation Loss")
    ax1.legend()
    ax1.grid(True, alpha=0.3)

    ax2.plot(history["train_acc"], label="Train Acc", linewidth=2)
    ax2.plot(history["val_acc"], label="Val Acc", linewidth=2)
    ax2.set_xlabel("Epoch")
    ax2.set_ylabel("Accuracy")
    ax2.set_title("Training & Validation Accuracy")
    ax2.legend()
    ax2.grid(True, alpha=0.3)

    # Mark stage boundary
    if "stage_boundary" in history:
        for ax in (ax1, ax2):
            ax.axvline(x=history["stage_boundary"], color="red",
                       linestyle="--", alpha=0.5, label="Fine-tune start")
            ax.legend()

    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"✓ Training curves saved to {output_path}")


def plot_confusion_matrix(y_true, y_pred, class_names, output_path):
    """Save a confusion matrix heatmap."""
    cm = confusion_matrix(y_true, y_pred, labels=range(len(class_names)))
    fig, ax = plt.subplots(figsize=(10, 8))
    sns.heatmap(
        cm, annot=True, fmt="d", cmap="Blues",
        xticklabels=class_names, yticklabels=class_names, ax=ax,
    )
    ax.set_xlabel("Predicted", fontsize=12)
    ax.set_ylabel("True", fontsize=12)
    ax.set_title("Confusion Matrix — FairFace Skin Tone Classifier", fontsize=14)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close()
    print(f"✓ Confusion matrix saved to {output_path}")


def print_evaluation_report(y_true, y_pred, class_names):
    """Print a full classification report."""
    print(f"\n{'='*60}")
    print("CLASSIFICATION REPORT")
    print(f"{'='*60}")
    print(classification_report(
        y_true, y_pred,
        target_names=class_names,
        labels=range(len(class_names)),
        zero_division=0,
    ))

    acc = accuracy_score(y_true, y_pred)
    prec, rec, f1, _ = precision_recall_fscore_support(
        y_true, y_pred, average="macro", zero_division=0
    )
    print(f"  Macro Accuracy:  {acc:.4f}")
    print(f"  Macro Precision: {prec:.4f}")
    print(f"  Macro Recall:    {rec:.4f}")
    print(f"  Macro F1:        {f1:.4f}")
    print(f"{'='*60}")


# ========================================================================
# MAIN
# ========================================================================

def main():
    args = parse_args()
    os.makedirs(args.output_dir, exist_ok=True)

    # ------------------------------------------------------------------
    # Device setup
    # ------------------------------------------------------------------
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*60}")
    print(f"DEVICE: {device}")
    if device.type == "cuda":
        print(f"  GPU: {torch.cuda.get_device_name(0)}")
        print(f"  Memory: {torch.cuda.get_device_properties(0).total_mem / 1e9:.1f} GB")
    print(f"{'='*60}\n")

    # ------------------------------------------------------------------
    # Datasets & DataLoaders
    # ------------------------------------------------------------------
    print("--- Loading datasets ---")
    train_ds = FairFaceDataset(
        args.data_csv, args.image_root,
        split="train", image_size=args.image_size,
        num_classes=args.num_classes,
    )
    val_ds = FairFaceDataset(
        args.data_csv, args.image_root,
        split="val", image_size=args.image_size,
        num_classes=args.num_classes,
    )

    train_loader = DataLoader(
        train_ds, batch_size=args.batch_size, shuffle=True,
        num_workers=args.num_workers, pin_memory=True, drop_last=True,
    )
    val_loader = DataLoader(
        val_ds, batch_size=args.batch_size, shuffle=False,
        num_workers=args.num_workers, pin_memory=True,
    )

    # ------------------------------------------------------------------
    # Model
    # ------------------------------------------------------------------
    print("\n--- Building EfficientNet-B4 model ---")
    model = build_model(num_classes=args.num_classes)
    model = model.to(device)
    total_params = sum(p.numel() for p in model.parameters())
    print(f"  Total parameters: {total_params:,}")

    # ------------------------------------------------------------------
    # Class weights & loss
    # ------------------------------------------------------------------
    class_weights = train_ds.get_class_weights().to(device)
    print(f"\n  Class weights: {class_weights.cpu().numpy().round(3)}")

    criterion = FocalLoss(gamma=2.0, alpha=0.25, weight=class_weights)

    # ------------------------------------------------------------------
    # Mixed precision scaler
    # ------------------------------------------------------------------
    scaler = GradScaler(enabled=(device.type == "cuda"))

    # ------------------------------------------------------------------
    # Training history
    # ------------------------------------------------------------------
    history = {
        "train_loss": [], "train_acc": [],
        "val_loss": [], "val_acc": [],
    }

    best_val_acc = 0.0
    epochs_no_improve = 0

    # ==================================================================
    # STAGE 1: Train head only
    # ==================================================================
    print(f"\n{'='*60}")
    print(f"STAGE 1: Head Training ({args.epochs_head} epochs)")
    print(f"{'='*60}")

    freeze_backbone(model)
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"  Trainable parameters: {trainable:,}")

    optimizer = optim.AdamW(
        filter(lambda p: p.requires_grad, model.parameters()),
        lr=1e-3,
    )

    for epoch in range(args.epochs_head):
        t0 = time.time()
        train_loss, train_acc = train_one_epoch(
            model, train_loader, criterion, optimizer, scaler, device
        )
        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, device)
        dt = time.time() - t0

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        improved = ""
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            torch.save(model.state_dict(),
                       os.path.join(args.output_dir, "best_head_model.pth"))
            improved = " ★ saved"

        print(
            f"  Epoch {epoch+1:02d}/{args.epochs_head}  "
            f"loss={train_loss:.4f}  acc={train_acc:.4f}  "
            f"val_loss={val_loss:.4f}  val_acc={val_acc:.4f}  "
            f"({dt:.1f}s){improved}"
        )

    # Load best head model before fine-tuning
    model.load_state_dict(
        torch.load(os.path.join(args.output_dir, "best_head_model.pth"),
                    map_location=device, weights_only=True)
    )

    # ==================================================================
    # STAGE 2: Fine-tune entire network
    # ==================================================================
    print(f"\n{'='*60}")
    print(f"STAGE 2: Full Fine-Tuning ({args.epochs_finetune} epochs)")
    print(f"{'='*60}")

    unfreeze_backbone(model)
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"  Trainable parameters: {trainable:,}")

    history["stage_boundary"] = args.epochs_head

    optimizer = optim.AdamW(model.parameters(), lr=5e-6, weight_decay=1e-5)
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(
        optimizer,
        T_0=10,         # First restart period
        T_mult=2,       # Double period after each restart
        eta_min=1e-7,
    )

    for epoch in range(args.epochs_finetune):
        global_epoch = args.epochs_head + epoch
        t0 = time.time()

        train_loss, train_acc = train_one_epoch(
            model, train_loader, criterion, optimizer, scaler, device
        )
        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, device)
        scheduler.step()
        dt = time.time() - t0

        history["train_loss"].append(train_loss)
        history["train_acc"].append(train_acc)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        improved = ""
        if val_acc > best_val_acc:
            best_val_acc = val_acc
            torch.save(model.state_dict(),
                       os.path.join(args.output_dir, "best_finetuned_model.pth"))
            improved = " ★ saved"
            epochs_no_improve = 0
        else:
            epochs_no_improve += 1

        print(
            f"  Epoch {global_epoch+1:02d}/{args.epochs_head + args.epochs_finetune}  "
            f"loss={train_loss:.4f}  acc={train_acc:.4f}  "
            f"val_loss={val_loss:.4f}  val_acc={val_acc:.4f}  "
            f"lr={optimizer.param_groups[0]['lr']:.2e}  "
            f"({dt:.1f}s){improved}"
        )

        # Early stopping
        if epochs_no_improve >= args.patience:
            print(f"\n  ⚠ Early stopping at epoch {global_epoch+1} "
                  f"(no improvement for {args.patience} epochs)")
            break

    # ------------------------------------------------------------------
    # Save final model
    # ------------------------------------------------------------------
    torch.save(model.state_dict(),
               os.path.join(args.output_dir, "final_model.pth"))
    print(f"\n✓ Final model saved to {os.path.join(args.output_dir, 'final_model.pth')}")

    # ==================================================================
    # EVALUATION
    # ==================================================================
    print(f"\n{'='*60}")
    print("FINAL EVALUATION (best fine-tuned model)")
    print(f"{'='*60}")

    # Load best model
    best_path = os.path.join(args.output_dir, "best_finetuned_model.pth")
    if os.path.exists(best_path):
        model.load_state_dict(
            torch.load(best_path, map_location=device, weights_only=True)
        )

    _, val_acc, val_preds, val_labels = evaluate(
        model, val_loader, criterion, device
    )

    class_names = val_ds.class_names

    # Classification report
    print_evaluation_report(val_labels, val_preds, class_names)

    # Confusion matrix
    plot_confusion_matrix(
        val_labels, val_preds, class_names,
        os.path.join(args.output_dir, "fairface_confusion_matrix.png"),
    )

    # Training curves
    plot_training_history(
        history,
        os.path.join(args.output_dir, "fairface_training_curves.png"),
    )

    print(f"\n{'='*60}")
    print(f"✓ Training complete!")
    print(f"  Best validation accuracy: {best_val_acc:.4f}")
    print(f"  Models saved to: {args.output_dir}")
    print(f"{'='*60}")


if __name__ == "__main__":
    main()
